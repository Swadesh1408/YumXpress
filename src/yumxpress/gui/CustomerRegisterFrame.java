    public boolean validateInputs(){
        char []pwd=this.custPass.getPassword();
        if(pwd.length==0||this.custName.getText().trim().isEmpty()||this.custEmail.getText().trim().isEmpty()||this.custMobile.getText().trim().isEmpty()||this.custAddress.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please fill all the details");
            return false;
        }
        if(Validator.isValidEmail(this.custEmail.getText().trim())==false){
             JOptionPane.showMessageDialog(null,"Please Input a valid Email Id");
             return false;
        }
        if(Validator.isValidMobile(this.custMobile.getText().trim())==false){
             JOptionPane.showMessageDialog(null,"Please Input a valid Mobile No");
             return false;
        }
        return true;
    }

