    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if(!validateInputs()){
            return;
        }
        try{
             CustomerPojo customer=new CustomerPojo();
             customer.setCustomerName(this.custName.getText().trim());
             customer.setCustomerEmailId(this.custEmail.getText().trim());
             customer.setCustomerMobile(this.custMobile.getText().trim());
             customer.setCustomerAddress(this.custAddress.getText().trim());
             char []pwd=this.custPass.getPassword();
             String pwdStr=new String(pwd);
             pwdStr=PasswordEncryption.getEncryptedPassword(pwdStr);
             customer.setCustomerPass(pwdStr);
             boolean dbResult=CustomerDao.addCustomer(customer);
             if(dbResult){
                JOptionPane.showMessageDialog(null,"Customer Registration Successfull"); 
                
                Map<String,String> emailCredentials=new HashMap<>();
                emailCredentials.put("emailId","swadesh.personal1534@gmail.com");
                emailCredentials.put("securityKey","liax vmqs zktc ekqh");
                Mailer.sendMail(emailCredentials, customer);
                JOptionPane.showMessageDialog(null,"Mail Sent to "+customer.getCustomerEmailId());
                clearAll();
                return;
             }else{
                 JOptionPane.showMessageDialog(null,"Customer Registration Unsuccessfull");
             }
         }catch(SQLException ex){
             JOptionPane.showMessageDialog(null,"DB Error in CustomerRegisterFrame");
             ex.printStackTrace();
         }
        catch(MessagingException ex){
             JOptionPane.showMessageDialog(null,"Mail Error in CustomerRegisterFrame");
             ex.printStackTrace();
         }
    }//GEN-LAST:event_btnRegisterActionPerformed

