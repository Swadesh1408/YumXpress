    private boolean validateInputs() {
        char []pwd=this.staffPass.getPassword();
        if(pwd.length==0||this.staffEmail.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please fill all the details");
            return false;
        }
        return true;
    }

    private void clearAll() {
        this.staffEmail.setText("");        
        this.staffPass.setText("");
        this.staffEmail.requestFocus();
    }
