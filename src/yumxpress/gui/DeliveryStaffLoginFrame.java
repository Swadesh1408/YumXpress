    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        boolean result=validateInputs();
        if(!result){
            return;
        }
        char []pwd=this.staffPass.getPassword();
        String pwdStr=new String(pwd);
        pwdStr=PasswordEncryption.getEncryptedPassword(pwdStr);
        try{
            StaffPojo staff=StaffDao.validate(staffEmail.getText().trim(),pwdStr);
            if(staff!=null){
                JOptionPane.showMessageDialog(null,"Login Successfull");
                clearAll();
                StaffDetails.setStaffEmail(staff.getEmailId());
                StaffDetails.setStaffName(staff.getStaffName());
                StaffDetails.setStaffId(staff.getStaffId());
                DeliveryStaffFrame custOpt=new DeliveryStaffFrame();
                custOpt.setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null,"Login Unsuccessfull");
                clearAll();
                return;
            }

        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB Error is SellerLoginFrame");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

