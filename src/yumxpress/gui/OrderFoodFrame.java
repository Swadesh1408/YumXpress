    private void companyNameListActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_companyNameListActionPerformed
        String compName=this.companyNameList.getSelectedItem().toString();
        String compId=companyMap.get(compName);
        if(compId==null){
            compId="All";
        }
        try{
            productList=ProductDao.getAllProductsByCompanyId(compId);
            showDetails();
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "DB Error in CustomerRegisterFrame");
            ex.printStackTrace();
        }catch(IOException ex){
            JOptionPane.showMessageDialog(null, "Image Error in CustomerRegisterFrame");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_companyNameListActionPerformed

    private void mouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_mouseClicked
        JLabel lbl=(JLabel)evt.getSource();
        int index=Integer.parseInt(lbl.getName());
        if(index>=productList.size()){
            JOptionPane.showMessageDialog(null, "Invalid Item Clicked");
            return;
        }
        String []str={"Add to Cart","Buy Now"};
        int choice=JOptionPane.showOptionDialog(null, "What do you want to do ?", "Select", JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null, str, str[1]);
        if(choice==0){
            ProductPojo p=productList.get(index);
            orderProductList.add(p);
        }
        else{
            ProductPojo p=productList.get(index);
            showFrame=new OrderProductFrame(p);
            showFrame.setVisible(true);
            this.dispose();
        }
        
        
    }//GEN-LAST:event_mouseClicked

