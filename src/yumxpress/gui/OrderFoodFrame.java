    private void loadCompanyNames() {
        try{
            companyMap=CompanyDao.getAllCompanyIdAndName();
            Set<String> companyNames=companyMap.keySet();
            this.companyNameList.addItem("All");
            for(String companyName:companyNames){
                this.companyNameList.addItem(companyName);
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "DB Error in CustomerRegisterFrame");
            ex.printStackTrace();
        }
    }

