    private void staffIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_staffIdActionPerformed
       String selectedStaffId=this.staffId.getSelectedItem().toString();
       try{
           StaffPojo staff=StaffDao.getStaffDetailsById(selectedStaffId);
           this.staffName.setText(staff.getStaffName());
           this.staffMail.setText(staff.getEmailId());
       }catch(SQLException ex){
           JOptionPane.showMessageDialog(null,"Db error in StaffActionPerformed");
       }
    }//GEN-LAST:event_staffIdActionPerformed

