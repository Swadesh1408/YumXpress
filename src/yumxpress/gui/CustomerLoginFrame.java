    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        boolean result=validateInputs();
        if(!result){
            return;
        }
        char []pwd=this.custPass.getPassword();
        String pwdStr=new String(pwd);
        pwdStr=PasswordEncryption.getEncryptedPassword(pwdStr);
        try{
            CustomerPojo customer=CustomerDao.validate(custEmail.getText().trim(),pwdStr);
            if(customer!=null){
                JOptionPane.showMessageDialog(null,"Login Successfull"); 
                clearAll();
                UserDetails.setCustEmailId(customer.getCustomerEmailId());
                UserDetails.setCustName(customer.getCustomerName());
                UserDetails.setCustId(customer.getCustomerId());
                CustomerOptionsFrame custOpt=new CustomerOptionsFrame();
                custOpt.setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(null,"Login Unsuccessfull");
                clearAll();
                return;
            }
            
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"DB Error is SellerLoginFrame");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnLoginActionPerformed

