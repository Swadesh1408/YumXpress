    private boolean validateInputs(){
        char []pwd=this.custPass.getPassword();
        if(pwd.length==0||this.custEmail.getText().trim().isEmpty()){
            JOptionPane.showMessageDialog(null,"Please fill all the details");
            return false;
        }
        return true;
    }

    private void clearAll() {
        this.custEmail.setText("");        
        this.custPass.setText("");
        this.custEmail.requestFocus();
    }
